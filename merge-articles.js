/* ========================================
   merge-articles.js — Merge article JSON files into content.js
   Usage: node merge-articles.js
   ======================================== */
const fs = require('fs');
const path = require('path');

const ARTICLES_DIR = path.join(__dirname, 'articles');
const CONTENT_JS_PATH = path.join(__dirname, 'js', 'content.js');

// Read all JSON files
const files = fs.readdirSync(ARTICLES_DIR).filter(f => f.endsWith('.json'));
console.log(`Found ${files.length} article JSON files.`);

const articles = [];
for (const file of files) {
  try {
    const raw = fs.readFileSync(path.join(ARTICLES_DIR, file), 'utf-8');
    // Strip BOM if present
    const cleaned = raw.charCodeAt(0) === 0xFEFF ? raw.slice(1) : raw;
    const article = JSON.parse(cleaned);
    articles.push(article);
    console.log(`  ✓ ${file}`);
  } catch (e) {
    console.error(`  ✗ ${file}: ${e.message}`);
  }
}

// Sort by date
articles.sort((a, b) => a.date.localeCompare(b.date));

// Generate the content.js file content
const categories = [
  { slug: "home-genius", en: "Home Genius", zh: "居家奇招", icon: "🏠", desc_en: "Unconventional cleaning, organizing, and DIY tricks", desc_zh: "不走寻常路的清洁、收纳和DIY奇招" },
  { slug: "money-dark-arts", en: "Money Dark Arts", zh: "省钱暗术", icon: "💰", desc_en: "Clever saving, negotiation scripts, and spending loopholes", desc_zh: "省钱的野路子、谈判话术和消费漏洞" },
  { slug: "body-hacks", en: "Body & Mind Hacks", zh: "健康偏方", icon: "🧠", desc_en: "Sleep, energy, focus, and health workarounds", desc_zh: "睡眠、精力、专注和健康的偏方解法" },
  { slug: "workplace-ninja", en: "Workplace Ninja", zh: "职场潜规则", icon: "💼", desc_en: "Career shortcuts, office survival, and getting ahead", desc_zh: "职场捷径、办公室生存和晋升暗术" },
  { slug: "social-cheats", en: "Social Cheats", zh: "社交外挂", icon: "🎯", desc_en: "Persuasion, communication, and getting what you want", desc_zh: "说服、沟通和得到你想要的社交作弊技巧" },
  { slug: "learning-exploits", en: "Learning Exploits", zh: "学习捷径", icon: "📚", desc_en: "Study shortcuts, memory tricks, and skill acquisition hacks", desc_zh: "学习投机取巧、记忆窍门和技能速成法" }
];

function formatArticle(a, index) {
  const related = JSON.stringify(a.related);
  // Escape body content for JS string
  const enBody = (a.en.body || '').replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$');
  const zhBody = (a.zh.body || '').replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$');
  const enTitle = (a.en.title || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const zhTitle = (a.zh.title || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const enSummary = (a.en.summary || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const zhSummary = (a.zh.summary || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const keywordsEn = (a.keywords_en || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const keywordsZh = (a.keywords_zh || '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");

  return `  {
    slug: '${a.slug}',
    category: '${a.category}',
    date: '${a.date}',
    image: '${a.image || ''}',
    keywords_en: '${keywordsEn}',
    keywords_zh: '${keywordsZh}',
    related: ${related},
    en: {
      title: '${enTitle}',
      summary: '${enSummary}',
      body: \`${enBody}\`
    },
    zh: {
      title: '${zhTitle}',
      summary: '${zhSummary}',
      body: \`${zhBody}\`
    }
  }`;
}

// Build the full content.js
let output = `/* ========================================
   Content Manifest — Categories & Article List
   Auto-generated by merge-articles.js
   ${new Date().toISOString().split('T')[0]}
   ======================================== */
const CATEGORIES = ${JSON.stringify(categories, null, 2)};

const ARTICLES = [
`;

const articleStrings = articles.map((a, i) => formatArticle(a, i));
output += articleStrings.join(',\n');

output += `
];

/* Node.js export for SSG build */
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { CATEGORIES: CATEGORIES, ARTICLES: ARTICLES };
}
`;

fs.writeFileSync(CONTENT_JS_PATH, output, 'utf-8');
console.log(`\n✅ Merged ${articles.length} articles into content.js`);
console.log(`   Output: ${CONTENT_JS_PATH}`);
console.log(`   Size: ${(fs.statSync(CONTENT_JS_PATH).size / 1024).toFixed(1)} KB`);
